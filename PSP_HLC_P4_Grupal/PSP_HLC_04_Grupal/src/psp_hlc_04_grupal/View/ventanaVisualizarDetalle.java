    public void cargarDatos() {
        ArrayList<Asignatura> n;
        try {
            n = desplazamientoDetalle.obtenerAsignaturas();
        } catch (SQLException ex) {
            Logger.getLogger(ventanaVisualizarDetalle.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (n != null) {
            textCod.setText(String.valueOf(n.get(0).getCodAsig()));
            textNom.setText(n.get(0).getNomAsig());
            System.out.println(n.get(0).getNomAsig());
            textNota.setText(String.valueOf(n.get(0).getNota()));
        } else {
            JOptionPane.showMessageDialog(this, "No hay datos disponibles.");
        }
    }
