    private void guardarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarNotaActionPerformed
        try {
            double nuevaNota = Double.parseDouble(textNota.getText());
            desplazamientoDetalle.actualizarNotaActual(nuevaNota);
            JOptionPane.showMessageDialog(this, "Nota actualizada correctamente.");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "La nota debe ser un número.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar la nota: " + ex.getMessage());
        }    
    }//GEN-LAST:event_guardarNotaActionPerformed


